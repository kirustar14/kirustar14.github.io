def parse_vcf_fast(vcf_path):
    """
    Fast VCF parser - extracts rsID, alleles, and dosage.
    Returns DataFrame with: snpid, ref, alt, alt_dosage
    """
    rows = []
    print(f"\n📖 Parsing VCF: {os.path.basename(vcf_path)}")

    with open(vcf_path, "r") as f:
        for line in f:
            if line.startswith("#"):
                continue

            fields = line.rstrip("\n").split("\t")
            if len(fields) < 10:
                continue

            rsid = fields[2].strip()
            ref = fields[3].strip().upper()
            alt = fields[4].strip().upper()

            # Skip missing rsID or multiallelic
            if rsid == "." or "," in alt:
                continue

            gt = fields[9].split(":")[0]
            if gt in ("0/0", "0|0"):
                alt_dosage = 0
            elif gt in ("0/1", "1/0", "0|1", "1|0"):
                alt_dosage = 1
            elif gt in ("1/1", "1|1"):
                alt_dosage = 2
            else:
                continue

            rows.append({"snpid": rsid, "ref": ref, "alt": alt, "alt_dosage": alt_dosage})

    df = pd.DataFrame(rows)
    df["snpid"] = df["snpid"].astype(str).str.strip()
    print(f"   ✅ Parsed {len(df):,} variants")
    return df

# Allele harmonization functions
_COMP = {"A":"T", "T":"A", "C":"G", "G":"C"}

def _complement(a: str) -> str:
    return "".join(_COMP.get(ch, "N") for ch in a)

def _ambiguous_pair(a: str, b: str) -> bool:
    s = {a, b}
    return s == {"A","T"} or s == {"C","G"}

def compute_effect_dosage(ref, alt, alt_dosage, ea, nea, drop_ambiguous=True):
    """
    Harmonize alleles and return effect allele dosage.
    Returns (effect_dosage, status)
    """
    ref, alt = ref.upper(), alt.upper()
    ea, nea = ea.upper(), nea.upper()

    # Drop strand-ambiguous SNPs
    if drop_ambiguous and _ambiguous_pair(ref, alt):
        return (np.nan, "ambiguous_ref_alt")

    # Direct alignment
    if ea == alt and nea == ref:
        return (alt_dosage, "ea_is_alt")
    if ea == ref and nea == alt:
        return (2 - alt_dosage, "ea_is_ref")

    # Strand flip
    refc, altc = _complement(ref), _complement(alt)
    if drop_ambiguous and _ambiguous_pair(refc, altc):
        return (np.nan, "ambiguous_after_flip")

    if ea == altc and nea == refc:
        return (alt_dosage, "ea_is_alt_flip")
    if ea == refc and nea == altc:
        return (2 - alt_dosage, "ea_is_ref_flip")

    return (np.nan, "no_match")

def calculate_disorder_prs_optimized(gwas_path, patient_df, disorder_name, verbose=False):
    """
    Calculate PRS for one disorder - FIXED VERSION
    """
    if verbose:
        print(f"\n🧬 Analyzing: {disorder_name}")

    patient_df = patient_df.copy()
    patient_df["snpid"] = patient_df["snpid"].astype(str).str.strip()
    patient_snps = set(patient_df["snpid"])

    # Column mapping for different GWAS formats
    col_map = {
        "rsid": "snpid", "snp": "snpid", "markername": "snpid", "id": "snpid",
        "a1": "ea", "effect_allele": "ea", "ea": "ea", "alt": "ea", "allele1": "ea",
        "a2": "nea", "other_allele": "nea", "nea": "nea", "ref": "nea", "allele2": "nea",
        "beta": "beta", "weight": "beta", "effect_weight": "beta", "effect": "beta",
        "or": "or", "odds_ratio": "or",
        "p": "p", "pval": "p", "p-value": "p"
    }

    matched = []

    # Read GWAS file - REMOVED low_memory parameter
    try:
        chunks = pd.read_csv(
            gwas_path,
            sep=r"\s+",
            engine="python",
            comment="#",
            chunksize=200000
        )
    except Exception as e:
        if verbose:
            print(f"   ❌ Error reading GWAS file: {e}")
        return 0.0

    for chunk in chunks:
        chunk.columns = chunk.columns.str.lower().str.strip()
        chunk = chunk.rename(columns=col_map)

        if "snpid" not in chunk.columns or "ea" not in chunk.columns or "nea" not in chunk.columns:
            continue

        # Pre-filter: only keep SNPs that are in patient VCF
        chunk["snpid"] = chunk["snpid"].astype(str).str.strip()
        chunk = chunk[chunk["snpid"].isin(patient_snps)]

        if not chunk.empty:
            matched.append(chunk)

    if not matched:
        if verbose:
            print("   ❌ No SNP matches found")
        return 0.0

    gwas = pd.concat(matched, ignore_index=True).drop_duplicates(subset=["snpid"])
    merged = patient_df.merge(gwas, on="snpid", how="inner")

    if verbose:
        print(f"   → Merged {len(merged)} SNPs between VCF and GWAS")

    # Create numeric weight
    if "beta" in merged.columns:
        merged["weight"] = pd.to_numeric(merged["beta"], errors="coerce")
    elif "or" in merged.columns:
        orv = pd.to_numeric(merged["or"], errors="coerce")
        merged["weight"] = np.log(orv)
    else:
        if verbose:
            print("   ❌ No beta or OR column found")
        return 0.0

    # Compute effect allele dosage
    eff = []
    for r in merged.itertuples(index=False):
        d, _ = compute_effect_dosage(r.ref, r.alt, r.alt_dosage, r.ea, r.nea, drop_ambiguous=True)
        eff.append(d)

    merged["ea_dosage"] = eff
    merged = merged.dropna(subset=["ea_dosage", "weight"])

    if verbose:
        print(f"   → After harmonization: {len(merged)} SNPs kept")

    if len(merged) == 0:
        if verbose:
            print("   ❌ No SNPs passed harmonization (alleles didn't match)")
        return 0.0

    # Calculate PRS
    score = float((merged["ea_dosage"] * merged["weight"]).sum())

    if verbose:
        print(f"   ✅ Final PRS: {score:.4f}")

    return score

