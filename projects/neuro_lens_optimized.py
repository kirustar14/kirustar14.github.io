def random_genotype():
    """70% ref, 25% het, 5% alt"""
    rand = random.random()
    if rand < 0.70:
        return "0/0"
    elif rand < 0.95:
        return "0/1"
    else:
        return "1/1"


def detect_columns(df):
    """
    Detect SNP ID and allele columns from DataFrame.
    Returns (snp_col, a1_col, a2_col) or (None, None, None)

    IMPROVED: True case-insensitive matching
    """
    # Create lowercase mapping
    cols_lower = {}
    for col in df.columns:
        cols_lower[col.lower().strip()] = col

    # SNP ID column - expanded patterns
    snp_col = None
    snp_patterns = [
        'snp', 'snpid', 'rsid', 'id', 'markername',
        'variant', 'rs', 'variant_id', 'snp_id'
    ]
    for pattern in snp_patterns:
        if pattern in cols_lower:
            snp_col = cols_lower[pattern]
            break

    # Allele 1 (effect allele)
    a1_col = None
    a1_patterns = [
        'a1', 'allele1', 'ea', 'effect_allele', 'alt',
        'tested_allele', 'allele 1', 'allele_1'
    ]
    for pattern in a1_patterns:
        if pattern in cols_lower:
            a1_col = cols_lower[pattern]
            break

    # Allele 2 (other allele)
    a2_col = None
    a2_patterns = [
        'a2', 'allele2', 'nea', 'other_allele', 'ref',
        'non_effect_allele', 'allele 2', 'allele_2'
    ]
    for pattern in a2_patterns:
        if pattern in cols_lower:
            a2_col = cols_lower[pattern]
            break

    return snp_col, a1_col, a2_col


def extract_snps_from_gwas_robust(filepath, n_samples=300, debug=False):
    """
    Robustly extract SNPs from GWAS file.
    Handles multiple formats and missing columns.
    """
    snps = []

    try:
        # Try reading with different separators
        df_sample = None
        for sep in [r'\s+', '\t', ',']:
            try:
                df_sample = pd.read_csv(
                    filepath,
                    sep=sep,
                    nrows=100,
                    comment='#',
                    engine='python'
                )
                if len(df_sample.columns) > 3:  # Looks valid
                    break
            except:
                continue

        if df_sample is None:
            print(f"   ❌ Could not read file")
            return []

        # Clean column names
        df_sample.columns = df_sample.columns.str.strip()

        if debug:
            print(f"   🔍 First few columns: {list(df_sample.columns[:15])}")

        # Detect columns
        snp_col, a1_col, a2_col = detect_columns(df_sample)

        if snp_col is None:
            print(f"   ⚠️  Could not detect SNP column")
            print(f"       Available columns: {list(df_sample.columns[:10])}")
            return []

        print(f"   ✓ Found SNP column: '{snp_col}'")

        # If alleles not found, use dummy values
        if a1_col is None or a2_col is None:
            print(f"   ⚠️  Allele columns not found")
            if a1_col:
                print(f"       Found A1: '{a1_col}'")
            if a2_col:
                print(f"       Found A2: '{a2_col}'")
            print(f"   → Using random alleles")
            use_dummy_alleles = True
        else:
            print(f"   ✓ Found alleles: '{a1_col}' and '{a2_col}'")
            use_dummy_alleles = False

        # Read larger sample
        df = pd.read_csv(
            filepath,
            sep=sep,
            nrows=20000,
            comment='#',
            engine='python'
        )
        df.columns = df.columns.str.strip()

        # Filter for valid rsIDs
        df_rsids = df[df[snp_col].astype(str).str.match(r'^rs\d+', na=False)]

        if debug:
            print(f"   🔍 Total rows read: {len(df)}")
            print(f"   🔍 Rows with valid rsIDs: {len(df_rsids)}")
            if len(df_rsids) > 0:
                print(f"   🔍 Sample rsIDs: {df_rsids[snp_col].head(3).tolist()}")

        # Sample
        if len(df_rsids) > n_samples:
            df_filtered = df_rsids.sample(n=n_samples, random_state=42)
        else:
            df_filtered = df_rsids

        # Extract SNPs
        for _, row in df_filtered.iterrows():
            rsid = str(row[snp_col]).strip()

            if use_dummy_alleles:
                # Assign random alleles
                alleles = ['A', 'T', 'C', 'G']
                ref = random.choice(alleles)
                alt_choices = [a for a in alleles if a != ref]
                alt = random.choice(alt_choices)
            else:
                try:
                    a1 = str(row[a1_col]).strip().upper()
                    a2 = str(row[a2_col]).strip().upper()

                    # Validate
                    if len(a1) == 1 and len(a2) == 1 and a1 in 'ACGT' and a2 in 'ACGT':
                        ref = a2
                        alt = a1
                    else:
                        # Invalid alleles, use random
                        alleles = ['A', 'T', 'C', 'G']
                        ref = random.choice(alleles)
                        alt_choices = [a for a in alleles if a != ref]
                        alt = random.choice(alt_choices)
                except:
                    # Error getting alleles, use random
                    alleles = ['A', 'T', 'C', 'G']
                    ref = random.choice(alleles)
                    alt_choices = [a for a in alleles if a != ref]
                    alt = random.choice(alt_choices)

            snps.append((rsid, ref, alt))

        return snps

    except Exception as e:
        print(f"   ❌ Error: {e}")
        import traceback
        traceback.print_exc()
        return []


